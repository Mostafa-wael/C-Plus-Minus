// Int conversion-------------------------------------------------------

// float to int
float f = 100.5;
int i = (int) f;
print(i);

// bool to int
bool b = true;
i = (int) b;
print(i);

// string to int
string s = "999";
i = (int) s;
print(i);

// Float conversion-------------------------------------------------------

// int to float
i = 100;
f = (float) i;
print(f);

// bool to float
b = true;
f = (float) b;
print(f);

// string to float
s = "999.9";
